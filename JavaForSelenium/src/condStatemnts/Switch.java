package condStatemnts;

public class Switch {

	public static void main(String[] args) {
		int x =5;
		switch(x)
		{
		case 1:
			System.out.println("Poor Result");
			break;
		case 2:
			System.out.println("Good Result");
			break;
		case 3:
			System.out.println("Excellent Result");
			break;
		case 4:
			System.out.println("You are the Topper");
			break;
		default:
			System.out.println("Failed");
		}

	}

}
